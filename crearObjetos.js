// Se pueden omitir las comillas en los nombres de las propiedades, si son de una sola palabra.

var miPerro = {
  nombre: "Cocoliso",
  edad: 18,
  peso: 26,
  raza: "Husky",
};

console.log(miPerro);

// Tambien se pueden usar numeros como propiedades de un objeto

var miObjeto = {
  5: "cinco",
};

console.log(miObjeto);
